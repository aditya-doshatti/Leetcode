class NestedIterator:
    def __init__(self, nestedList: [NestedInteger]):
        self.flatVal, self.cur = [], 0
        self.convert(nestedList)

    def convert(self, nestedList):
        for lst in nestedList:
            if lst.isInteger():
                self.flatVal.append(lst.getInteger())
            else:
                self.convert(lst.getList())
    
    def next(self) -> int:
        if self.hasNext():
            retVal =  self.flatVal[self.cur]
            self.cur += 1
            return retVal
    
    def hasNext(self) -> bool:
         return len(self.flatVal) > self.cur

